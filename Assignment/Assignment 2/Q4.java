public class Q4 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("Hello");
        }

        System.out.println();

        int i = 0;
        while (i < 10){
            System.out.println("Hello");
            i++;
        }

        System.out.println();
        i = 0;
        do {
            System.out.println("Hello");
            i++;
        }while (i < 10);
    }
}
